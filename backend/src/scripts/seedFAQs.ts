            question: "Как я получу уведомления о чрезвычайных ситуациях?",
            answer: "Убедитесь, что у приложения есть разрешение на отправку уведомлений. Вы будете получать push-уведомления о чрезвычайных ситуациях, актуальных новостях и предупреждениях в вашем регионе.",
